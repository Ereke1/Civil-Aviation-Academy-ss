                - усилить работу психологических служб ТиПО г. Алматы;<br/>
                - повысить осведомленность социально - психологических служб и педагогических работников по выбору критериев работы по обеспечению условий безопасной среды в работе со студентами и поддержания психического благополучия.<br/>
                Данное мероприятие позволяет объединить усилия работников системы здравоохранения и системы образования по оказанию условий безопасной среды. <br/>
